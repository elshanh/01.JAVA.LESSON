package az.project.lesson03.nonPrimitive;

public class NonPrimitive {
	
	public static void main(String[] args) {
		
		/*
			Strings: 
			Arrays: 
			Classes: 
			Interface: 
			*/
		
			String  StringType = "Java";
			System.out.println(StringType);
			String[] ArrayType = {"Volvo", "BMW", "Ford", "Mazda"};
			System.out.println(ArrayType[0]);
			System.out.println(ArrayType[3]);
			System.out.println(ArrayType[1]);
			System.out.println(ArrayType[2]);
	}
	
}
